int	check_path(char *path)
{
	int	i;

	i = 0;
	while (path[i])
		i++;
	if (path[i - 1] != 'b' && path[i - 2] != 'u'
		&& path[i - 3] != 'c' && path[i - 4] != '.')
		return (1);
	return (0);
}
